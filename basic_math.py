# basic math operator with my own value


def main():
    print("5+3 = {}".format(5 + 3))
    print("8-3 = {}".format(8 - 4))
    print("12*6 = {}".format(12 * 6))
    print("7/2 = {}".format(7 / 3))
    print("2^3 = {}".format(2 ^ 3))  # bitwise operator
    print("3**3 = {}".format(3**3))  # exponent
    print("81**0.5 = {}".format(81**0.5))  # square root


if __name__ == "__main__":
    main()
